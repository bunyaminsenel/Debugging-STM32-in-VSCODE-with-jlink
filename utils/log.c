/************************************************************************
*
* FUNCTION: 	Log_PrintByteArray
*
************************************************************************/
void Log_PrintByteArray(uint8_t *buf, uint32_t len)
{
  while (len--)
  {
    LOG_RAW("%02x ", *buf++);
  }
  LOG_RAW("\n");
}
